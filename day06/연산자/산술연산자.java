package 연산자;

public class 산술연산자 {
	
	public static void main(String[] args) {
//		산술 연산자 ( 사칙연산자 , + - / * % : 나머지)
		int x = 20;
		int y = 10;
		
		System.out.println("더한 값은 " +(x + y));
		System.out.println("뺀 값은 " +(x - y));
		System.out.println("곱한 값은 " +(x * y));
		System.out.println("나눈 값은 " +(x / y));
		System.out.println("나머지 값은 " +(x % y));
	}
}
